package Ejemplos;
class FormulasX{
    public int mulSum(int m, int n, int o){
        return m+(n*o);
    }
    public int divRes(int m,int n, int o){
        return m-(n/o);
    }
    public int mod(int m, int n){
        return m%n;
    }
    public int inc(int n){
        n++;
        return n    ;
    }
}